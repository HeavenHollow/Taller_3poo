public class Matematicas {
    public static int suma(int a, int b) { //metodo de suma 
        return a + b;
    }

    public static int resta(int a, int b) { // metodo de resta
        return a - b;
    }

    public static int multiplicacion(int a, int b) { // metodo de multiplicacion
        return a * b;
    }

    public static double division(double a, double b) { // metodo de division 
        return a / b;
    }
    
}
